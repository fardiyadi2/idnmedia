    var initSchedules = function (cityVal, currentDay = 1) {
        // console.log('cityVal: ');
        // console.log(cityVal);
        // console.log('currentDay: ');
        // console.log(currentDay);
        var scheduleData;
        $.each(schedules, function( key, city ) {
            $.each(city, function( key, values ) {
                if (cityVal == key) {
                    scheduleData = values;
                    return false;
                }
            });
        });
        // console.log('scheduleData: ');
        // console.log(scheduleData);
        var html = '';
        $.each(scheduleData.schedules, function( key, value ) {
            html += '<div>'+
                '<span class="the-day">' + value.TANGGAL + '</span>'+
                '<div class="wts-container clearfix">'+
                    '<div class="item wts-imsak">Imsak<span>' + value.IMSAK + '</span></div>'+
                    '<div class="item wts-subuh">Subuh<span>' + value.SUBUH + '</span></div>'+
                    '<div class="item wts-dzuhur">Dzuhur<span>' + value.ZUHUR + '</span></div>'+
                    '<div class="item wts-ashar">Ashar<span>' + value.ASAR + '</span></div>'+
                    '<div class="item wts-maghrib">Maghrib<span>' + value.MAGRIB + '</span></div>'+
                    '<div class="item wts-isya">Isya<span>' + value.ISYA + '</span></div>'+
                '</div>'+
            '</div>';
        });
        if ($('.wts-day').hasClass('slick-initialized')) {
            $('.wts-day').slick('unslick');
        }
        $('.wts-day').html(html).slick();
        // slideIndex = $(this).index();
        slideIndex = parseInt(currentDay) <= 0 ? 1 : (parseInt(currentDay) - 1);
        // console.log('slideIndex: ');
        // console.log(slideIndex);
        $( '.wts-day' ).slick('slickGoTo', parseInt(slideIndex) );
    };



