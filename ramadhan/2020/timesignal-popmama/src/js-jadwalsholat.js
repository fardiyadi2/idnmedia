    var initSchedules = function (cityVal, currentDay = 1) {
        // console.log('cityVal: ');
        // console.log(cityVal);
        // console.log('currentDay: ');
        // console.log(currentDay);
        var scheduleData;
        $.each(schedules, function( key, city ) {
            $.each(city, function( key, values ) {
                if (cityVal == key) {
                    scheduleData = values;
                    return false;
                }
            });
        });
        // console.log('scheduleData: ');
        // console.log(scheduleData);
        var html = '';
        $.each(scheduleData.schedules, function( key, value ) {
            html += '<div>'+
                '<span class="the-day">' + value.TANGGAL + '</span>'+
                '<dl class="clearfix">'+
                    '<dt>Imsyak</dt><dd>' + value.IMSAK + '</dd>'+
                    '<dt>Subuh</dt><dd>' + value.SUBUH + '</dd>'+
                    '<dt>Dhuhur</dt><dd>' + value.ZUHUR + '</dd>'+
                    '<dt>Ashar</dt><dd>' + value.ASAR + '</dd>'+
                    '<dt>Maghrib</dt><dd>' + value.MAGRIB + '</dd>'+
                    '<dt>Isya</dt><dd>' + value.ISYA + '</dd>'+
                '</dl>'+
            '</div>';
        });
        if ($('.wts-day').hasClass('slick-initialized')) {
            $('.wts-day').slick('unslick');
        }
        $('.wts-day').html(html).slick();
        // slideIndex = $(this).index();
        slideIndex = parseInt(currentDay) <= 0 ? 1 : (parseInt(currentDay) - 1);
        // console.log('slideIndex: ');
        // console.log(slideIndex);
        $( '.wts-day' ).slick('slickGoTo', parseInt(slideIndex) );
    };

    

